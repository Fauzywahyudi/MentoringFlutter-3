    if (value.isEmpty) return 'Required';
    return null;
  }

  String validatePenulis(String value) {
    if (value.isEmpty) return 'Required';
    return null;
  }

  String validateSumber(String value) {
    if (value.isEmpty) return 'Required';
    return null;
  }
}
